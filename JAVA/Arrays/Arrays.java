import java.util.*;

public class Arrays {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int [] subMarks = new int [3];

        subMarks[0] = 98;
        subMarks[1] = 88;
        subMarks[2] = 74;

       for(int i = 0; i < 3; i++){
           System.out.println(subMarks[i]);
       }



        sc.close();

    }
}


// output:

// 98
// 88
// 74




















